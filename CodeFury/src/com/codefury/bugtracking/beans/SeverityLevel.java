package com.codefury.bugtracking.beans;

/**
 * Enumeration representing the severity levels of bugs.
 */
public enum SeverityLevel {
    /**
     * Indicates a trivial severity level for a bug.
     */
    TRIVIAL,

    /**
     * Indicates a minor severity level for a bug.
     */
    MINOR,

    /**
     * Indicates a major severity level for a bug.
     */
    MAJOR,

    /**
     * Indicates a critical severity level for a bug.
     */
    CRITICAL
}
